package com.saru.java;

/*
 * 
 * Interface - Adjective -> Runnable, Readable - Pascal Case
 * Class - Noun -> Student, Dog - Pascal Case
 * Method - Verb -> run, show - Camel Case
 * Variable -> price, purchasePrice - Camel Case
 * Constant -> PI , API_KEY - Snake Case
 * 
 */

public class VariableDemo 
{

	public static void main(String[] args) 
	{
		byte b = 10;
		short s = 100;
		int i = 5;
		long l = 1000000000L;
		float f = 10.20F;
		double d = 10.5;
		char c = 'A';
		
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
	}

}
